package com.lib.sdk.next.gps;

/**
 * FileName: CoordEntry
 * Author: zhikai.jin
 * Date: 2021/6/16 15:36
 * Description: 坐标系
 */
public class CoordEntry {

    private double x = 0;

    private double y = 0;

    private double theta = 0;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getTheta() {
        return theta;
    }

    public void setTheta(double theta) {
        this.theta = theta;
    }
}
